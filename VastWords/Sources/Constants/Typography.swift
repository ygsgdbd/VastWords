import SwiftUI

/// 全局字体样式常量
enum Typography {
    /// 正文文字
    static let body = Font.body
    /// 标题文字
    static let title = Font.headline
    /// 副标题文字
    static let subtitle = Font.subheadline
    /// 按钮文字
    static let button = Font.callout
    /// 小标签文字
    static let caption = Font.caption
} 