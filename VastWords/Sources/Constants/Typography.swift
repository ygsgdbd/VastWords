import SwiftUI

/// 全局字体样式常量
enum Typography {
    /// 正文文字 (13)
    static let body = Font.system(size: 13)
    /// 标题文字 (12)
    static let title = Font.system(size: 12)
    /// 副标题文字 (11)
    static let subtitle = Font.system(size: 11)
    /// 按钮文字 (10)
    static let button = Font.system(size: 10)
    /// 小标签文字 (9)
    static let caption = Font.system(size: 9)
} 