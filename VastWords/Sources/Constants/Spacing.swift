import Foundation

/// 全局间距常量
enum Spacing {
    /// 无间距 (0)
    static let none: CGFloat = 0
    /// 超小间距 (2)
    static let tiny: CGFloat = 2
    /// 小间距 (4)
    static let small: CGFloat = 4
    /// 中间距 (6)
    static let medium: CGFloat = 6
    /// 大间距 (8)
    static let large: CGFloat = 8
    /// 超大间距 (12)
    static let extraLarge: CGFloat = 12
} 